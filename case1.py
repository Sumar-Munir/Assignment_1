total_miles = 0
total_gallons = 0

while True:
    miles = float(input("Enter No. of Miles travelled: (-1 to quit): "))
    if miles == -1:
        break
    gallons = float(input("Enter No. of Gallons used: "))
    if gallons > 0:
        mpg = miles / gallons
        print(f"MPG: {mpg:.6f}") 
    else:
        print("Sorry, we cant move with zero gallons.")

    total_miles += miles
    total_gallons += gallons

if total_gallons > 0:
    Total_mpg = total_miles / total_gallons
    print(f"Total Miles per Gallon: {Total_mpg:.6f}")
else:
    print("No data entered.") 


